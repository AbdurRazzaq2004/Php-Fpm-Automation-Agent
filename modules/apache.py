    def __init__(self, system: SystemDetector, log: DeployLogger):
        self.system = system
        self.log = log
        self.os_info = system.detect_os()

    def _run(self, cmd: str) -> Tuple[int, str, str]:
        result = subprocess.run(
            cmd, shell=True, capture_output=True, text=True, timeout=60
        )
        return result.returncode, result.stdout.strip(), result.stderr.strip()

    def _enable_required_modules(self, language: str = "php"):
        """
        Enable required Apache modules.
        For PHP: proxy_fcgi. For other languages: proxy_http.
        """
        if self.os_info["family"] != "debian":
            return

        required_modules = [
            "rewrite",      # URL rewriting
            "headers",      # Security headers
            "expires",      # Static asset caching
            "deflate",      # Gzip compression
            "ssl",          # SSL/TLS support
            "setenvif",     # ProxyFCGI env vars
        ]

        if language == "php":
            required_modules.append("proxy_fcgi")
        elif language == "static":
            pass  # No proxy needed
        else:
            # Reverse proxy for all other languages
            required_modules.extend(["proxy", "proxy_http", "proxy_wstunnel"])

        enabled = []
        for mod in required_modules:
            rc, _, _ = self._run(f"a2enmod {mod} 2>/dev/null")
            if rc == 0:
                enabled.append(mod)

        if enabled:
            self.log.info(f"Enabled Apache modules: {', '.join(enabled)}")

    # ── Paths ───────────────────────────────────────────────────

    def _get_paths(self) -> Dict:
        """Get correct Apache config paths for this distro."""
        if self.os_info["family"] == "debian":
            return {
                "sites_available": "/etc/apache2/sites-available",
                "sites_enabled": "/etc/apache2/sites-enabled",
                "use_symlink": True,
                "enable_cmd": "a2ensite",
                "disable_cmd": "a2dissite",
            }
        else:
            return {
                "conf_d": "/etc/httpd/conf.d",
                "use_symlink": False,
            }

    # ── Config Generation ───────────────────────────────────────

    def generate_vhost(self, config: Dict, backup: BackupManager) -> bool:
        """Generate Apache VirtualHost configuration.
        Handles PHP-FPM, reverse proxy, and static site configs."""
        service_name = config["service_name"]
        domain = config["domain"]
        enable_ssl = config.get("enable_ssl", False)
        language = config.get("language", "php")

        self.log.step(f"Generating Apache vhost for [{service_name}] ({language})")

        paths = self._get_paths()

        if paths.get("use_symlink"):
            config_path = os.path.join(paths["sites_available"], f"{service_name}.conf")
        else:
            config_path = os.path.join(paths["conf_d"], f"{service_name}.conf")

        # Safety check
        if os.path.exists(config_path):
            with open(config_path, "r") as f:
                existing = f.read()
            if f"VirtualHost: {service_name}" in existing:
                self.log.info("Updating existing vhost config")
                backup.backup_file(config_path)
            else:
                self.log.error(
                    f"Vhost config exists but wasn't created by this agent: {config_path}"
                )
                self.log.error("Refusing to overwrite foreign config")
                return False

        # Ensure required Apache modules are enabled
        self._enable_required_modules(language)

        # Convert upload size to bytes for Apache LimitRequestBody
        max_upload = config.get("max_upload_size", "64M")
        max_bytes = self._parse_size_to_bytes(max_upload)

        # Conditional ServerAlias (skip for IP addresses)
        is_ip = bool(re.match(r'^(\d{1,3}\.){3}\d{1,3}$', domain))
        server_alias_line = "" if is_ip else f"    ServerAlias www.{domain}"

        # Route to correct template builder
        if language == "php":
            content = self._generate_php_vhost(config, server_alias_line, max_bytes, enable_ssl)
        elif language == "static":
            content = self._generate_static_vhost(config, server_alias_line, max_bytes, enable_ssl)
        else:
            content = self._generate_proxy_vhost(config, server_alias_line, max_bytes, enable_ssl)

        # Write config
        try:
            os.makedirs(os.path.dirname(config_path), exist_ok=True)
            with open(config_path, "w") as f:
                f.write(content)
            os.chmod(config_path, 0o644)
            self.log.success(f"Vhost written: {config_path}")
        except Exception as e:
            self.log.error(f"Failed to write vhost: {e}")
            return False

        # Enable site
        if paths.get("use_symlink"):
            rc, _, err = self._run(f"a2ensite {service_name}.conf")
            if rc != 0:
                self.log.warn(f"a2ensite warning: {err}")
            self.log.info(f"Site enabled via a2ensite")

        return True

    # ── Template Builders ───────────────────────────────────────

    def _generate_php_vhost(self, config, server_alias_line, max_bytes, enable_ssl):
        """Generate PHP-FPM vhost (original behavior)."""
        domain = config["domain"]
        tmpl_vars = {
            "service_name": config["service_name"],
            "domain": domain,
            "server_alias_line": server_alias_line,
            "php_version": config.get("php_version", config.get("runtime_version", "8.2")),
            "fpm_socket": config.get("fpm_socket", ""),
            "document_root": config["document_root"],
            "max_upload_bytes": max_bytes,
            "extra_config": config.get("extra_apache_config", ""),
        }
        if enable_ssl:
            ssl_cert = config.get("ssl_cert_path", f"/etc/letsencrypt/live/{domain}/fullchain.pem")
            ssl_key = config.get("ssl_key_path", f"/etc/letsencrypt/live/{domain}/privkey.pem")
            tmpl_vars["ssl_cert_path"] = ssl_cert
            tmpl_vars["ssl_key_path"] = ssl_key
            content = self.VHOST_SSL_REDIRECT.format(**tmpl_vars)
            content += "\n"
            content += self.VHOST_SSL_TEMPLATE.format(**tmpl_vars)
        else:
            content = self.VHOST_HTTP_TEMPLATE.format(**tmpl_vars)
        return content

    def _generate_proxy_vhost(self, config, server_alias_line, max_bytes, enable_ssl):
        """Generate reverse proxy vhost for non-PHP languages."""
        domain = config["domain"]
        client_build_path = config.get("_client_build_path")

        tmpl_vars = {
            "service_name": config["service_name"],
            "domain": domain,
            "server_alias_line": server_alias_line,
            "language": config.get("language", "app"),
            "app_port": config.get("app_port", 3000),
            "document_root": config.get("document_root", config.get("deploy_path", "")),
            "max_upload_bytes": max_bytes,
            "extra_config": config.get("extra_apache_config", ""),
        }

        # Fullstack app (React/Vue/Angular frontend + API backend)
        if client_build_path:
            tmpl_vars["client_build_path"] = client_build_path
            self.log.info(f"Using fullstack vhost: static from {client_build_path} + API proxy")
            content = self.FULLSTACK_HTTP_TEMPLATE.format(**tmpl_vars)
            return content

        if enable_ssl:
            ssl_cert = config.get("ssl_cert_path", f"/etc/letsencrypt/live/{domain}/fullchain.pem")
            ssl_key = config.get("ssl_key_path", f"/etc/letsencrypt/live/{domain}/privkey.pem")
            tmpl_vars["ssl_cert_path"] = ssl_cert
            tmpl_vars["ssl_key_path"] = ssl_key
            content = self.VHOST_SSL_REDIRECT.format(**tmpl_vars)
            content += "\n"
            content += self.REVERSE_PROXY_SSL_TEMPLATE.format(**tmpl_vars)
        else:
            content = self.REVERSE_PROXY_HTTP_TEMPLATE.format(**tmpl_vars)
        return content

    def _generate_static_vhost(self, config, server_alias_line, max_bytes, enable_ssl):
        """Generate static site vhost."""
        tmpl_vars = {
            "service_name": config["service_name"],
            "domain": config["domain"],
            "server_alias_line": server_alias_line,
            "document_root": config.get("document_root", config.get("deploy_path", "")),
            "extra_config": config.get("extra_apache_config", ""),
        }
        content = self.STATIC_HTTP_TEMPLATE.format(**tmpl_vars)
