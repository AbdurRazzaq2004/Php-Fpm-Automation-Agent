    # ── Config Validation & Reload ──────────────────────────────

    def test_config(self) -> bool:
        """Test Apache configuration syntax."""
        self.log.info("Testing Apache configuration...")
        success, output = self.system.apache_config_test()
        if success:
            self.log.success("Apache configuration test passed")
        else:
            self.log.error(f"Apache configuration test FAILED:\n{output}")
        return success

    def safe_reload(self) -> bool:
        """Safely reload Apache."""
        self.log.step("Safely reloading Apache")

        if not self.test_config():
            self.log.error("Aborting reload - configuration invalid")
            return False

        apache_cmd = self.system.get_apache_command()

        if self.system.is_apache_running():
            rc, _, err = self._run(f"systemctl reload {apache_cmd}")
            if rc != 0:
                self.log.error(f"Apache reload failed: {err}")
                return False
        else:
            rc, _, err = self._run(f"systemctl start {apache_cmd}")
            if rc != 0:
                self.log.error(f"Apache start failed: {err}")
                return False

        if not self.system.is_apache_running():
            self.log.error("Apache is not running after reload")
            return False

        self.log.success("Apache reloaded successfully")
        return True

    # ── Removal (for rollback) ──────────────────────────────────

    def remove_vhost(self, service_name: str) -> bool:
        """Remove a vhost configuration."""
        paths = self._get_paths()

        if paths.get("use_symlink"):
            self._run(f"a2dissite {service_name}.conf 2>/dev/null")
            for p in [
                os.path.join(paths["sites_available"], f"{service_name}.conf"),
                os.path.join(paths["sites_enabled"], f"{service_name}.conf"),
            ]:
                if os.path.exists(p) or os.path.islink(p):
                    os.unlink(p)
                    self.log.info(f"Removed: {p}")
        else:
            p = os.path.join(paths["conf_d"], f"{service_name}.conf")
            if os.path.exists(p):
                os.unlink(p)
                self.log.info(f"Removed: {p}")
        return True

    # ── Helpers ──────────────────────────────────────────────────

    @staticmethod
    def _parse_size_to_bytes(size_str: str) -> int:
        """Convert '64M' style sizes to bytes."""
        size_str = size_str.strip().upper()
        multipliers = {"K": 1024, "M": 1024**2, "G": 1024**3}
        for suffix, mult in multipliers.items():
            if size_str.endswith(suffix):
                return int(size_str[:-1]) * mult
        return int(size_str)
